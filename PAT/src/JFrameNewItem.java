    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
       boolean valid = false;
            if (TextFieldItemDescription.getText().isEmpty() || TextFieldItemDescription.getText().length() < 2 || jComboBoxStockLeft.getSelectedItem().toString().equals("null") || jComboBoxCostPrice.getSelectedItem().toString().equals("null") || jComboBoxSellingPrice.getSelectedItem().toString().equals("null") || jComboBoxItemsSold.getSelectedItem().toString().equals("null") || jDateChooserlastOrder.getDate().toString ().isEmpty()) { // checks to see if the user has enetered all values 
                JOptionPane.showMessageDialog(null, "Please Check that you have entered all the details correctly"); // Displays a message to tell the user to check their inputÃ
            } else {
                valid = true;
            }
        

        if (valid == true) {
            try {

                String description = TextFieldItemDescription.getText(); // Gets and stores the description from the field
                int StockLeft = Integer.parseInt(jComboBoxStockLeft.getSelectedItem().toString()); // Stores what the user has selected into the variable
                double CostPrice = Double.parseDouble(jComboBoxCostPrice.getSelectedItem().toString()); // Stores what the user has selected into the variable
                double SellingPrice = Double.parseDouble(jComboBoxSellingPrice.getSelectedItem().toString()); // Stores what the user has selected into the variable
                int SoldMonthly = Integer.parseInt(jComboBoxItemsSold.getSelectedItem().toString());// Stores what the user has selected into the variable
                SimpleDateFormat dcn = new SimpleDateFormat("dd/MM/yyyy");// sets the format of the date
                String LastOrder = dcn.format(jDateChooserlastOrder.getDate());// Gets the date from the calendar and stores iy ito the variable
                arr.InsertItem(description, StockLeft, CostPrice, SellingPrice, SoldMonthly, LastOrder); // creates and inserts the new item (object)
                JOptionPane.showMessageDialog(null, "The item has been added"); // A message is displayed to the user

            } catch (FileNotFoundException ex) {
                Logger.getLogger(JFrameNewItem.class.getName()).log(Level.SEVERE, null, ex);
            }

            this.hide(); // sets this frame to be invisible
            JFrameMainMenu p = new JFrameMainMenu(); // creates a new main menu jFrame
            p.show(); // displays main menu
        }
    }//GEN-LAST:event_jButtonNextActionPerformed

